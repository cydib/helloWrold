import time


def count_time(func):
    def wrapper(*args):
        t1 = time.time()
        func(*args)
        t2 = time.time()
        print("total time {:.2}".format(t2 - t1))

    return wrapper


@count_time
def add(max):
    sum = 0
    for i in range(0, max):
        sum += i
    print(sum)


