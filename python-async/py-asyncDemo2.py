import time
import asyncio
import warnings

warnings.filterwarnings('ignore')


async def test_main(x):
    # time.sleep(2)
    await asyncio.sleep(2)
    print(x)


async def main():
    t0 = time.time()
    await asyncio.wait([
        test_main("a"),
        test_main("b"),
        test_main("c"),
    ])
    t1 = time.time()
    print("Took %.2f ms" % (1000 * (t1 - t0)))


loop = asyncio.get_event_loop()
loop.run_until_complete(main())
loop.close()
